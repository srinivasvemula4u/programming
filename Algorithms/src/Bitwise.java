import java.io.*;
import java.util.Scanner;
/**
 *
 * @author sv186040
 */
public class Bitwise {
    public static void main(String[] args) {
        Scanner s = new Scanner(System.in);
        int n = s.nextInt();
        System.out.println(n);
        System.out.println(~n+1);
        
    }
}
